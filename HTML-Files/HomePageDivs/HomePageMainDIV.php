            <h1> A FORMAL DEFINITION OF ‘FUNDAMENTAL RIGHTS’   </h1>
            <p>
                At the beginning of his essay, Ferrajoli announces that he intends to
elaborate a formal definition of the concept of ‘fundamental rights’.‘Fundamental rights’, as we know, is a metajuridical category, as the
notion does not belong to the normative lexicon used in any constitutional text, nor in any modern legal system. The term ‘fundamental rights’
(Grundrechte) is used generically by several European legal theorists to
mean ‘fundamental freedoms’.4 Anglo-American political philosophy, on
the other hand – and I am thinking of John Rawls, for example – uses
the expression ‘basic liberties’ to describe the tradition that goes from
‘habeas corpus’ to the contemporary liberal democratic constitutions.5
Ronald Dworkin also usually expresses himself in terms of ‘rights’ (or
at the most of ‘moral rights’), without any further qualifications.6 On the
other hand, an author like Gregorio Peces-Barba Martínez, who devoted a
weighty volume to derechos fondamentales, trying to elaborate a general
theory covering both legal and moral aspects of rights, declares that he uses
the notion as the substantial equivalent of that of the ‘rights of man’ (which
has been rather more frequent in political and legal language since the 1948
Universal Declaration).7 With very few exceptions, then, it can be said that
the leading theorists of law and of politics, including Norberto Bobbio,
very seldom use the expression ‘fundamental rights’.8 And when they use
it, they do so to designate, without any formal sophistication, the principal
rights of freedom that modern constitutions have recognised as tools for
protecting certain (individual or group) interests, values or expectations
ever since the great bourgeois revolutions.
Ferrajoli had already tacked the topic of ‘fundamental rights’, both in
his Teoria assiomatizzata del diritto and in Diritto e ragione.
9 In these
two books, he maintained the need to distinguish clearly between ‘fundamental rights’, as capable of “satisfying persons’ values and achieving
their equality”, and ‘property rights’ (rights of ownership, rights of credit
etc.), considered to be the source of ‘legal inequality’ between actors.
While “fundamental rights are conditions of each individual’s identity as
a person or as a citizen” and are therefore “inviolable, inalienable and
non-disposable”, property rights – as Ferrajoli defines them – establish
“relationships of dominion and of subjection, i.e. of power and of exclusion
between legally unequal actors.”10 By stressing the “radical difference in
structure” between the two types of rights, Ferrajoli aimed to promote a
“conceptual and linguistic reclamation” with regard to the promiscuous
use made of the term ‘subjective rights’, which in his opinion had been the
“vehicle of a century of mystification” perpetrated by the liberal tradition
and, in reflection, also the Marxist one.
In the opening pages of his new essay, Ferrajoli seems to abandon the
ethical and political motivations that enlivened his project for ‘reclaiming’
the theoretical lexicon of lawyers in his previous writings. He even seems
to want to put a certain distance between himself and any historicalpolitical and ideological perspective. He declares that his definition of
‘fundamental rights’ is irrespective of ‘de facto situations’, i.e. of “the
nature of the interests and of the needs that are protected by their recognition as fundamental rights.”12 And he also intends to disregard their
‘positive’ nature, i.e. the fact that provision is made for them by a legal
system that is effectively in force. He intends to base his reasoning solely
on the universal character of their attribution “in the purely logical and
non-evaluative sense of universal quantification.”
